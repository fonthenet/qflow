const DICTIONARIES: Record<DesktopLocale, Messages> = {
  en: {},
  fr: FR,
  ar: AR,
};

export function normalizeLocale(locale: string | null | undefined): DesktopLocale {
  if (!locale) return 'en';
  const normalized = locale.toLowerCase().split('-')[0];
  return (DESKTOP_LOCALES as readonly string[]).includes(normalized) ? (normalized as DesktopLocale) : 'en';
}

export function getDirection(locale: DesktopLocale): 'ltr' | 'rtl' {
  return locale === 'ar' ? 'rtl' : 'ltr';
}

export function interpolate(template: string, values?: Record<string, string | number | null | undefined>): string {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ''));
}

export function t(locale: DesktopLocale, key: string, values?: Record<string, string | number | null | undefined>): string {
  const template = DICTIONARIES[locale][key] ?? key;
  return interpolate(template, values);
}

export function formatDesktopTime(value: string | Date, locale: DesktopLocale): string {
  const date = typeof value === 'string' ? new Date(value) : value;
  const tag = locale === 'fr' ? 'fr-FR' : locale === 'ar' ? 'ar' : 'en-US';
  return new Intl.DateTimeFormat(tag, { hour: 'numeric', minute: '2-digit' }).format(date);
}

export function formatWaitLabel(dateStr: string, locale: DesktopLocale): string {
  const mins = Math.floor((Date.now() - new Date(dateStr).getTime()) / 60000);
  if (mins < 1) {
    return locale === 'fr' ? '<1 min' : locale === 'ar' ? '<1 د' : '<1m';
  }
  if (mins < 60) {
    return locale === 'fr' ? `${mins} min` : locale === 'ar' ? `${mins} د` : `${mins}m`;
  }
  const hours = Math.floor(mins / 60);
  const rest = mins % 60;
  if (locale === 'fr') return `${hours} h ${rest} min`;
  if (locale === 'ar') return `${hours} س ${rest} د`;
  return `${hours}h ${rest}m`;
}

export function desktopLanguageLabel(locale: DesktopLocale, value: DesktopLocale): string {
  const labels: Record<DesktopLocale, Record<DesktopLocale, string>> = {
    en: { en: 'English', fr: 'French', ar: 'Arabic' },
    fr: { en: 'Anglais', fr: 'Français', ar: 'Arabe' },
    ar: { en: 'الإنجليزية', fr: 'الفرنسية', ar: 'العربية' },
  };
  return labels[locale][value];
}
